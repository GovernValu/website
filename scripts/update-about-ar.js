async function updateAboutContent() {
    console.log('Updating Arabic About page content...');

    await prisma.pageContent.upsert({
        where: {
            page_lang: {
                page: 'about',
                lang: 'ar'
            }
        },
        update: {
            content: aboutContent
        },
        create: {
            page: 'about',
            lang: 'ar',
            content: aboutContent
        }
    });

    console.log('Successfully updated About page content!');
}

updateAboutContent()
